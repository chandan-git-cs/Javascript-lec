// Boolean Operation
// AND  ,  OR  , NOT 

// AND [ && ]
//  true  && true  = true
//  true  && false = false
//  false && true = false
//  false && false = false

// OR [ || ]
//  true  || true  = true
//  true  || false = true
//  false || true = true
//  false || false = false

// NOT [ ! ]

let a = true;
console.log(!a);

let b = false;
console.log(!b);